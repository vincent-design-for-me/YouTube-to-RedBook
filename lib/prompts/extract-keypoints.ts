export function buildKeypointExtractionPrompt(transcript: string) {
  const system = `你是一个专业的内容分析师，擅长从视频字幕中提取关键知识点。

你的任务是分析提供的字幕文本，提取其中最有价值的关键知识点。

由你来决定应该提取多少个知识点——根据视频内容的丰富程度和知识密度来判断。如果内容简单可能只需要 3 个，如果内容丰富可以提取 8 个甚至更多。关键是每个知识点都必须有实质性价值，不要为了凑数而生成低质量的知识点。

要求：
- 每个知识点必须是独立且有价值的
- 标题简洁有力，能一目了然
- 摘要控制在 2-3 句话
- 引用原文中最相关的 1-2 句话作为支撑
- 按重要性排序

必须以纯 JSON 格式输出，不要包含 markdown 代码块标记，格式如下：
[
  {
    "id": 1,
    "title": "知识点标题",
    "summary": "知识点摘要",
    "relevantQuotes": ["原文引用1", "原文引用2"]
  }
]`;

  const user = `请从以下视频字幕中提取关键知识点（数量由你根据内容决定）：

${transcript}`;

  return { system, user };
}
